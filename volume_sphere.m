function V = volume_sphere(ff,vv)
    A = vv(ff(:,1),:);
    B = vv(ff(:,2),:);
    C = vv(ff(:,3),:);
    V = A(:,1).*((B(:,2)-A(:,2)).*(C(:,3)-A(:,3)) - (C(:,2)-A(:,2)).*(B(:,3)-A(:,3)))...
        -A(:,2).*((B(:,1)-A(:,1)).*(C(:,3)-A(:,3)) - (C(:,1)-A(:,1)).*(B(:,3)-A(:,3)))...
        +A(:,3).*((B(:,1)-A(:,1)).*(C(:,2)-A(:,2)) - (C(:,1)-A(:,1)).*(B(:,2)-A(:,2)));
    V = (1/6)*sum(V);
end
